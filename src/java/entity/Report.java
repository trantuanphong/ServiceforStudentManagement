package entity;

import java.util.Date;

/**
 *
 * @author ADMIN
 */
public class Report {
    int id;
    int senderID;
    String title;
    String detail;
    Date date;
    String status;

    public Report() {
    }

    public Report(int id, int senderID, String title, String detail, Date date, String status) {
        this.id = id;
        this.senderID = senderID;
        this.title = title;
        this.detail = detail;
        this.date = date;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSenderID() {
        return senderID;
    }

    public void setSenderID(int senderID) {
        this.senderID = senderID;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

   
    
}
